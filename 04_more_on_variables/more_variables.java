class more_variables

{
	public static void main(String[] args){



		float decimal_number = 45.092f;
		System.out.println("The temperature outside is currently: " + decimal_number);

		boolean judgement = true;
		System.out.println("Is pizza delicious? Survey says: " + judgement);

		byte byte_example = 127;
		System.out.println("A byte looks like: " + byte_example);

		short short_number = 30000;
		System.out.println("I'm a short. I look like: " + short_number);

		long long_number = 2140000001;
		System.out.println("I'm a long. Space Shuttle is currently " + long_number + " miles away from Earth.");

		double long_double = 5.1231243124123;
		System.out.println("I'm a double. We have exactly: " + long_double + " seconds until we land.");		


	}




}

