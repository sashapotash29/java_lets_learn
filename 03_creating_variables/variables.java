class variables
{

	public static void main(String[] args)
		{
			// STRING
			String word = "Hello I am a STRING";
			System.out.println("STRING EXAMPLE:");
			System.out.println(word);

			// CHARACTER
			char character ='C';
			System.out.println("CHAR EXAMPLE:");
			System.out.println(character);

			// INTEGER
			int number = 29;
			System.out.println("INTEGER EXAMPLE:");
			System.out.println(number);

			// BOTH INTEGER AND STRING
			System.out.println("I am a string, but I'm adding a integer called " + number);

	}

}